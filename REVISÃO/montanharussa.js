// Se a altura da pessoa for maior ou igual a altura mínima e menor que altura máxima: Permitido, senão: Não Permitido

let alturaPessoa = 1.77;
let alturaMinima = 1.45;
let alturaMaxima = 2.01;

if (alturaPessoa >= alturaMinima && alturaPessoa < alturaMaxima) {
    console.log("Permitido!");
} else {
    console.log("Não Permitido!")
} 